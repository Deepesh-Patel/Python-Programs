# Create a list using []
a = [1, 2 , 4, 68, 6, 7, 88]

# Print the list using print() function
print(a)

# Access using index using a[0], a[1], a[2]
print(a[4])

# Change the value of list using
a[0] = 70
print(a)

# We can create a list with items of different types
c = [45, "Buddy", False, 6.9]
print(c)
